function findIdElement(target, maxDepth = 3) {
    let current = target;
    let depth = 0;

    while (current && depth <= maxDepth) {
        if (current.id) return current;
        current = current.parentElement;
        depth++;
    }

    return null;
}

function trackPageViewAndIdentifyUser() {

    const path = window.location.pathname;

    mixpanel.track("Entro en " + path);
    
    if (path === '/mi-cuenta' || path === '/signup/confirmation') {
        const userInformation = localStorage.getItem("userLUCStorageTracking");

        if (userInformation) {
            const userInformationJson = JSON.parse(userInformation);

            const distinctId = userInformationJson.mail + "-" + userInformationJson.userId;

            mixpanel.identify(distinctId);

            mixpanel.people.set({
                '$email': userInformationJson.mail,
                '$name': userInformationJson.fullName
            });
        }
    }
    
}

document.addEventListener("click", function (e) {
    let id = e.target.id;

    if (!id) {
        const elementWithId = findIdElement(e.target, 3);

        if (!elementWithId || !elementWithId.id) return;

        id = elementWithId.id;
    }

    const event = Events[id];
    if (event) {
        mixpanel.track(event);
    }

    if (id === "btn-cerrar-sesion") {
        mixpanel.reset();
    }
});

document.addEventListener("DOMContentLoaded", () => {
    mixpanel.track("Abrio Luc");
    trackPageViewAndIdentifyUser();
});

(function () {
    const originalPushState = history.pushState;
    const originalReplaceState = history.replaceState;

    function onRouteChange() {
        trackPageViewAndIdentifyUser();
    }

    history.pushState = function () {
        originalPushState.apply(this, arguments);
        onRouteChange();
    };

    history.replaceState = function () {
        originalReplaceState.apply(this, arguments);
        onRouteChange();
    };

    window.addEventListener("popstate", onRouteChange);
})();
